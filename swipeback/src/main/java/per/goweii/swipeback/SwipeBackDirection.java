package per.goweii.swipeback;

/**
 * 滑动返回的方向
 */
public enum SwipeBackDirection {
    /**
     * 禁用滑动返回
     */
    NONE,
    /**
     * 向右侧滑动返回，即从屏幕右侧划出
     */
    RIGHT,
    /**
     * 向底部滑动返回，即从屏幕底部划出
     */
    BOTTOM,
    /**
     * 向左侧滑动返回，即从屏幕左侧划出
     */
    LEFT,
    /**
     * 向顶部滑动返回，即从屏幕顶部划出
     */
    TOP,
    ;
}